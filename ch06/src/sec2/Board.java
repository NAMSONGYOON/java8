package sec2;

public class Board {
	int pno;
	String ptitle;
	String pcontents;
	String writer;
	int pdate;
	
	public Board () { }
	public Board (int pno){this.pno = pno; }
	public Board(int pno, String ptitle, String pcontents, String writer, int pdate) {
		this.pno = pno; this.ptitle = ptitle; this.pcontents = pcontents; this.writer = writer; this.pdate = pdate;
	}
	
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public String getPtitle() {
		return ptitle;
	}
	public void setPtitle(String ptitle) {
		this.ptitle = ptitle;
	}
	public String getPcontents() {
		return pcontents;
	}
	public void setPcontents(String pcontents) {
		this.pcontents = pcontents;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public int getPdate() {
		return pdate;
	}
	public void setPdate(int pdate) {
		this.pdate = pdate;
	}
	
	
}
